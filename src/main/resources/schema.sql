CREATE TABLE IF NOT EXISTS users (
  id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);
CREATE TABLE IF NOT EXISTS items (
  id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR,
  available BOOLEAN,
  owner INT NOT NULL,
  lease_count INT,
  CONSTRAINT pk_item PRIMARY KEY (id),
  CONSTRAINT item_owner_fk FOREIGN KEY (owner) REFERENCES PUBLIC.users(ID)
);
CREATE TABLE IF NOT EXISTS booking (
  id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  start_date TIMESTAMP NOT NULL,
  end_date TIMESTAMP NOT NULL,
  item_id INT NOT NULL,
  requester_id INT NOT NULL,
  status VARCHAR(255) NOT NULL,
  CONSTRAINT pk_booking PRIMARY KEY (id),
  CONSTRAINT item_booking_fk FOREIGN KEY (item_id) REFERENCES PUBLIC.items(ID),
  CONSTRAINT user_booking_fk FOREIGN KEY (requester_id) REFERENCES PUBLIC.users(ID)
);
CREATE TABLE IF NOT EXISTS comments (
  id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  user_id INT NOT NULL,
  item_id INT NOT NULL,
  comment VARCHAR NOT NULL,
  created TIMESTAMP NOT NULL,
  CONSTRAINT pk_comment PRIMARY KEY (id),
  CONSTRAINT comment_item_fk FOREIGN KEY (item_id) REFERENCES PUBLIC.items(ID),
  CONSTRAINT comment_user_fk FOREIGN KEY (user_id) REFERENCES PUBLIC.users(ID)
);